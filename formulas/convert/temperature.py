def celsius_to_fahrenheit(x):
    """
    Function to convert Celsius to Fahrenheit
    :param x: input in Celsius
    :return: Converted to Fahrenheit
    """
    return (x * (9 / 5)) + 32


def celsius_to_kelvin(x):
    """
    Function to convert Celsius to Kelvin
    :param x: input in Celsius
    :return: Converted to Fahrenheit
    """
    return x + 273.15
